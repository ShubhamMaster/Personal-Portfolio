<script>
       let onlineFetched = false;
let checkInterval = 1000; // Start with fast checks (1 sec)
let failureCount = 0;

function fetchOnlineDate() {
    if (onlineFetched) return; // Stop fetching after success

    let isFetched = false;

    // Use system date if request takes too long
    setTimeout(() => {
        if (!isFetched) {
            document.getElementById("age").innerHTML = ` ${calculateAge(new Date())} ðŸ”´ Offline Date`;
        }
    }, 800);

    let xhr = new XMLHttpRequest();
    xhr.open("GET", "https://www.timeapi.io/api/Time/current/zone?timeZone=UTC", true);
    xhr.onreadystatechange = function () {
        if (xhr.readyState === 4 && xhr.status === 200) {
            const data = JSON.parse(xhr.responseText);
            const today = new Date(data.year, data.month - 1, data.day);
            isFetched = true;
            onlineFetched = true; // Stop further requests
            document.getElementById("age").innerHTML = `${calculateAge(today)}  ðŸŸ¢ Online Date  `;
        } else {
            failureCount++;
        }
    };
    xhr.send();

    // After 10 seconds of failures, slow down requests to every 30 sec
    if (failureCount >= 10) {
        checkInterval = 30000;
    }
}

// Start checking
let checkLoop = setInterval(() => {
    if (onlineFetched) clearInterval(checkLoop); // Stop once online date is found
    else fetchOnlineDate();
}, checkInterval);

function calculateAge(today) {
    const birthDate = new Date("2005-07-30");
    let age = today.getFullYear() - birthDate.getFullYear();
    const monthDiff = today.getMonth() - birthDate.getMonth();
    if (monthDiff < 0 || (monthDiff === 0 && today.getDate() < birthDate.getDate())) {
        age--;
    }
    return age + " years";
}

